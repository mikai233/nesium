// PIXEL00 opcodes: 0(C), 1(1M), 2(1U), 3(1L), 4(2), 5(4), 6(5)
#[inline(always)]
fn render_p00(dst: &mut u32, w: &[u32; 10], op: u8) {
    match op {
        0 => *dst = w[5],
        1 => *dst = interp1(w[5], w[1]),
        2 => *dst = interp1(w[5], w[2]),
        3 => *dst = interp1(w[5], w[4]),
        4 => *dst = interp2(w[5], w[4], w[2]),
        5 => *dst = interp4(w[5], w[4], w[2]),
        6 => *dst = interp5(w[4], w[2]),
        _ => unreachable!(),
    }
}

